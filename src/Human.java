public class Human extends Object{


    String name;
    String surname;
    String email;
    int phoNo;
    boolean gender;
    int birthYear;
    public Human() {
    }

    public Human(String name, String surname, String email, int phoNo, boolean gender, int birthYear) {
        this.name = name;
        this.surname = surname;
        this.email = email;
        this.phoNo = phoNo;
        this.gender = gender;
        this.birthYear = birthYear;
    }

}
